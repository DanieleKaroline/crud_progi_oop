    private void btn_singupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_singupActionPerformed
       DBController database = new DBController();
       
       String cpf = edt_cpf.getText();
       String email = edt_email.getText();
       String senha = edt_senha.getText();
       String nome = edt_nome.getText();
       String endereco = edt_endereco.getText();
       int telefone = Integer.parseInt(edt_telefone.getText());
       
       try {
            ChecksData newUser = new ChecksData(cpf, email, senha, nome, endereco);
            database.conectar();
            database.cad_us(cpf, email, senha, nome, endereco, telefone);
            
            JOptionPane.showMessageDialog(this, "Usu√°rio Cadastrado!");
            database.desconectar();
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        } finally {
            JComponent comp = (JComponent) evt.getSource();             //para fechar o login dps de logar
            Window win = SwingUtilities.getWindowAncestor(comp);
            win.dispose();
        }
    }//GEN-LAST:event_btn_singupActionPerformed

    
